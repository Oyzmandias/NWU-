package com.ozy.seckill.entity;

public class VenuePlan {
    private String useStatusName;
    private String sort;
    private String addUserStatus;
    private int useStatus;
    private boolean checkStatus;
    private String price;
    private int disable;
    private int venueId;
    private String name;
    private int id;
    private String time;
    private int venuePlanId;
    private Object campusInfoId;

    public String getUseStatusName() {
        return useStatusName;
    }

    public void setUseStatusName(String useStatusName) {
        this.useStatusName = useStatusName;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getAddUserStatus() {
        return addUserStatus;
    }

    public void setAddUserStatus(String addUserStatus) {
        this.addUserStatus = addUserStatus;
    }

    public int getUseStatus() {
        return useStatus;
    }

    public void setUseStatus(int useStatus) {
        this.useStatus = useStatus;
    }

    public boolean isCheckStatus() {
        return checkStatus;
    }

    public void setCheckStatus(boolean checkStatus) {
        this.checkStatus = checkStatus;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getDisable() {
        return disable;
    }

    public void setDisable(int disable) {
        this.disable = disable;
    }

    public int getVenueId() {
        return venueId;
    }

    public void setVenueId(int venueId) {
        this.venueId = venueId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getVenuePlanId() {
        return venuePlanId;
    }

    public void setVenuePlanId(int venuePlanId) {
        this.venuePlanId = venuePlanId;
    }

    public Object getCampusInfoId() {
        return campusInfoId;
    }

    public void setCampusInfoId(Object campusInfoId) {
        this.campusInfoId = campusInfoId;
    }

}
